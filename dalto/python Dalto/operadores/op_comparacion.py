#operadores de comparacion
# == es igual que
# != es distinto que
# < es menor que 
# <= es menor o igual que 
# > es mayor que 
# >= es mayor o igual que

es_igual_que = 5 == 6 #devuelve True o False
distinto_de = 5 != 6
mayor_que = 5 > 6
menor_que = 5 < 6
mayor_o_igual = 5 >= 6
menor_o_igual = 5 <= 6

print(es_igual_que)
print(distinto_de)
print(mayor_que)
print(menor_que)
print(mayor_o_igual)
print(menor_o_igual)